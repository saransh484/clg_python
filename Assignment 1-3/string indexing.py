# indexing
string = "Programming with python"

# +ve indexing
print("the 0th letter of string'",string ,"'is : ",string[0])
print("the 2nd letter of string'",string ,"'is : ",string[2])
print("the 4th letter of string'",string ,"'is : ",string[4])
print("the 8th letter of string'",string ,"'is : ",string[8])

# -ve indexing
print("the -1st letter of string'",string ,"'is : ",string[-1])
print("the -4th letter of string'",string ,"'is : ",string[-4])
print("the -8th letter of string'",string ,"'is : ",string[-8])
