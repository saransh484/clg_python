#string methods

string = "Programming With Python"

print("Original string : " + string)
print("using lower() method : " + string.lower())
print("using upper() method : " + string.upper())
print("using len() function length of string is :" , len(string))
print("using count() method number of 'python' sting : " , string.count("Python"))
print("using replace() method replacing 'With' with 'of' : " , string.replace("With","of"))
