# print corresponding day of that number using if-elif-else

x = int(input("Enter number : "))

if x == 1:
    print("it's monday")
elif x == 2:
    print("it's tuesday")
elif x == 3:
    print("it's wednesday")
elif x == 4:
    print("it's thursday")
elif x == 5:
    print("it's friday")
elif x == 6:
    print("it's saturday")
else:
    print("it's sunday")
